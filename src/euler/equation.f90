SUBROUTINE InitEquation()
!===================================================================================================================================
! Init Equations
!===================================================================================================================================
! MODULES
USE MOD_Equation_Vars
USE MOD_Readintools,ONLY:GETREAL,GETINT,GETLOGICAL
!-----------------------------------------------------------------------------------------------------------------------------------
! IMPLICIT VARIABLE HANDLING
!-----------------------------------------------------------------------------------------------------------------------------------
! INPUT VARIABLES
!-----------------------------------------------------------------------------------------------------------------------------------
! OUTPUT VARIABLES
!-----------------------------------------------------------------------------------------------------------------------------------
! LOCAL VARIABLES 
!===================================================================================================================================

! Readin of the equation parameters (TBD what to be read in here)
WRITE(*,*)
WRITE(*,*) '-[Constants]------------------------------------------------'
WRITE(*,*) '| Using Euler equations.'
! Specific heat ratio
gamma = GETREAL('gamma','1.4')
R = GETREAL('R','287')
! Flux Function
iFlux = GETINT('FluxFunction')
! General constants
Pi        = acos(-1.)
