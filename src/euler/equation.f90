gamma2    = gamma - 2.
gamma1q   = 1./gamma1
sqrt2     = SQRT(2.)
sqrt3     = SQRT(3.)
sqrt3_q   = 1./SQRT(3.)
! required for Calulation of time step
mu        = 0.
!Prandtl_R = (9.*gamma-5.)/(4.*gamma1)*R
Pr        = 0.72
! Readin of Source
CalcSource=GETLOGICAL('CalcSource','F')
IF (CalcSource) THEN
  SourceFunc=GETINT('SourceFunction')
END IF
