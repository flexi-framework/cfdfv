MODULE MOD_Equation
!===================================================================================================================================
! Module Equations
!===================================================================================================================================
! MODULES
!-----------------------------------------------------------------------------------------------------------------------------------
! IMPLICIT VARIABLE HANDLING
IMPLICIT NONE
!-----------------------------------------------------------------------------------------------------------------------------------
! GLOBAL VARIABLES
!-----------------------------------------------------------------------------------------------------------------------------------
INTERFACE InitEquation
  MODULE PROCEDURE InitEquation
END INTERFACE
!-----------------------------------------------------------------------------------------------------------------------------------
PUBLIC :: InitEquation
!===================================================================================================================================

CONTAINS

SUBROUTINE InitEquation()
!===================================================================================================================================
! Init Equations
!===================================================================================================================================
! MODULES
USE MOD_Equation_Vars
USE MOD_Readintools,ONLY:GETREAL,GETINT,GETLOGICAL
!-----------------------------------------------------------------------------------------------------------------------------------
! IMPLICIT VARIABLE HANDLING
!-----------------------------------------------------------------------------------------------------------------------------------
! INPUT VARIABLES
!-----------------------------------------------------------------------------------------------------------------------------------
! OUTPUT VARIABLES
!-----------------------------------------------------------------------------------------------------------------------------------
! LOCAL VARIABLES 
!===================================================================================================================================

! Readin of the equation parameters (TBD what to be read in here)
WRITE(*,*)
WRITE(*,*) '-[Constants]------------------------------------------------'
WRITE(*,*) '| Using Navier-Stokes equations.'
! Specific heat ratio
gamma = GETREAL('gamma','1.4')
R = GETREAL('R','287')
! Set viscosity and Prandtl number for viscous terms
mu = GETREAL('mu','0.')
Pr = GETREAL('Pr','0.72')   ! reference Prandtl number
! Flux Function
iFlux = GETINT('FluxFunction')
! General constants
Pi        = acos(-1.)
gamma1    = gamma - 1.
gamma2    = gamma - 2.
gamma1q   = 1./gamma1
sqrt2     = SQRT(2.)
sqrt3     = SQRT(3.)
sqrt3_q   = 1./SQRT(3.)
!Prandtl_R = (9.*gamma-5.)/(4.*gamma1)*R
! Readin of Source
CalcSource=GETLOGICAL('CalcSource','F')
IF (CalcSource) THEN
  SourceFunc=GETINT('SourceFunction')
END IF
!-----------------------------------------------------------------------------------------------------------------------------------
END SUBROUTINE InitEquation

END MODULE MOD_Equation
